enum class AppLanguage(val key: String, val tag: String, val nativeName: String) {
    SYSTEM("system", "", "System"),
    ZH_CN("zh-CN", "zh-CN", "简体中文"),
    ZH_TW("zh-TW", "zh-TW", "繁體中文"),
    EN("en", "en", "English"),
    JA("ja", "ja", "日本語"),
    RU("ru", "ru", "Русский"),
    KO("ko", "ko", "한국어");

    companion object {
        fun fromKey(key: String?): AppLanguage =
            entries.firstOrNull { it.key.equals(key, ignoreCase = true) } ?: SYSTEM

        fun resolveEffective(pref: AppLanguage): AppLanguage {
            if (pref != SYSTEM) return pref
            val loc = Locale.getDefault()
            val lang = loc.language.lowercase(Locale.ROOT)
            val country = loc.country.uppercase(Locale.ROOT)
            return when {
                lang == "zh" && (country == "TW" || country == "HK" || country == "MO") -> ZH_TW
                lang == "zh" -> ZH_CN
                lang == "ja" -> JA
                lang == "ru" -> RU
                lang == "ko" -> KO
                lang == "en" -> EN
                else -> ZH_CN
            }
        }
    }
}

