object L10nCatalog {
    fun forLanguage(pref: AppLanguage): L10n {
        val eff = AppLanguage.resolveEffective(pref)
        val tag = when (eff) {
            AppLanguage.ZH_CN -> "zh-CN"
            AppLanguage.ZH_TW -> "zh-TW"
            AppLanguage.EN -> "en"
            AppLanguage.JA -> "ja"
            AppLanguage.RU -> "ru"
            AppLanguage.KO -> "ko"
            AppLanguage.SYSTEM -> "zh-CN"
        }
        return L10n(PACKS[tag] ?: FALLBACK)
    }
}

val LocalL10n = staticCompositionLocalOf { L10nCatalog.forLanguage(AppLanguage.ZH_CN) }

